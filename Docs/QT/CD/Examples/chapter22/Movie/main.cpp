#include <QtGui>

// ----------------------------------------------------------------------
int main(int argc, char** argv)
{
    QApplication app(argc, argv);    
    QLabel       lbl;
    QMovie       mov("motion.mng");

    lbl.setMovie(&mov);
    lbl.resize(328, 270);
    lbl.show();
    mov.start();

    return app.exec();
}

