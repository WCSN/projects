/**
 * @mainpage MT library.
 * @section Introduction Introduction.
 * There are common tools for comfortable programming.
 *
 * @section  Compilation "Compilation".
 * The cmake is used for compilation.
 * @subsection  Cmake_Flags Cmake Flags.
 * <ul>
 *   <li> <b>WITH_MYSQL</b>. If the flag set to YES than mysql will be included to mtlib. The flag set to NO by default.</li>
 *
 *   <li> <b>MYSQL_PATH</b>. The flag specify the path of mysql's includes and  the client library. Default path is <i>/usr/local/mysql/</i>. Includes MUST be in <i>MYSQL_PATH/include</i> and the library MUST be in <i>MYSQL_PATH/lib/mysql</i> </li>
 *
 *   <li> <b>WITH_SMPP</b>. If this flag set to YES, smpp protocol supporting will be included to mtlib. The flag set to NO by default.</li>
 *
 *   <li> <b>WITHOUT_TEST</b>. If this flag set to YES, no testers will be compiled. The flag set to NO by default.</li>
 *
 *   <li> <b>INSTALL_PREFIX</b>. Set location to install. The path <i>/usr/local/mtlib</i> is used by default.</li>
 *
 *   <li> <b>CMAKE_BUILD_TYPE</b>. Set build type. Set 'Release' for release build or 'Debug' for debug build. </li>
 * </ul>
 */

